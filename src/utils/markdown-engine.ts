/**
 * MarkdownEngine — Assembles the final README.md from all collected data.
 *
 * This is the **final output pipeline**. It receives structured data from
 * every stage of the analysis pipeline (metadata, tech stack, commands,
 * features, APIs, AI-generated content, directory tree) and produces a
 * polished, deterministic README.md.
 *
 * Features:
 *   • Deterministic section ordering with graceful degradation
 *   • Badge generation from TechMapper data
 *   • Command tables from CommandInference results
 *   • Feature lists from FeatureExtractor results
 *   • API endpoint tables from APIExtractor results
 *   • AI content integration with fallback sections
 *   • Configurable output style
 *   • Enhanced Markdown formatting with tables, lists, code blocks, links, emojis
 *   • Professional visual structure with proper spacing and hierarchy
 *
 * Data flow:
 *   `MetadataExtractor`    → project name, description, license, repo
 *   `TechMapper`           → project types, languages → badges
 *   `CommandInference`     → validated commands → command table
 *   `FeatureExtractor`     → validated features → feature list
 *   `APIExtractor`         → validated endpoints → API table
 *   `AIEngine`             → AI-generated overview, usage, etc.
 *   `TreeGenerator`        → ASCII directory tree
 *
 * @module utils/markdown-engine
 */

