// ─────────────────────────── Types ───────────────────────────

/**
 * Complete input data required by the MarkdownEngine.
 */
export interface EngineInput {
  /** Project metadata (name, version, description, etc.) */
  metadata: ProjectMetadata;
  /** Detected project types and language breakdown */
  techReport: TechReport;
  /** Validated commands from inference pipeline */
  inferenceResult?: InferenceResult | null;
  /** Validated features from extraction pipeline */
  featureResult?: FeatureExtractionResult | null;
  /** Validated API endpoints from extraction pipeline */
  apiResult?: APIExtractionResult | null;
  /** AI-generated content sections (may be partial) */
  aiContent?: AIContent | null;
  /** ASCII directory tree string */
  tree?: string;
  /** Pre-sanitized data from DataSanitizer (used when available) */
  sanitizedData?: SanitizedData | null;
  /** Badge style to use throughout */
  badgeStyle?: BadgeStyle;
}

/**
 * AI-generated content sections (optional, may be partial).
 */
export interface AIContent {
  /** AI-generated project overview */
  overview?: string;
  /** AI-generated features section */
  features?: string;
  /** AI-generated installation instructions */
  installation?: string;
  /** AI-generated usage examples */
  usage?: string;
  /** AI-generated API documentation */
  apiDocs?: string;
  /** Full AI-generated README (used as-is if complete, parsed for sections otherwise) */
  fullReadme?: string;
}

/**
 * Configuration for the MarkdownEngine.
 */
export interface EngineConfig {
  /** Whether to include the directory tree section */
  includeTree: boolean;
  /** Whether to include the command table section */
  includeCommands: boolean;
  /** Whether to include the features section */
  includeFeatures: boolean;
  /** Whether to include the API endpoints section */
  includeAPIs: boolean;
  /** Whether to include badges (shields.io) */
  includeBadges: boolean;
  /** Whether to include a "Generated by" footer */
  includeFooter: boolean;
  /** Maximum number of commands to display */
  maxCommands: number;
  /** Maximum number of features to display */
  maxFeatures: number;
  /** Maximum number of API endpoints to display */
  maxAPIs: number;
  /** Template name being used */
  template?: string;
  /** List of section IDs to include in output */
  sections?: SectionId[];
  /** Badge style to use throughout */
  badgeStyle?: BadgeStyle;
}

/** Default configuration. */
const DEFAULT_CONFIG: Readonly<EngineConfig> = Object.freeze({
  includeTree: true,
  includeCommands: true,
  includeFeatures: true,
  includeAPIs: true,
  includeBadges: true,
  includeFooter: true,
  maxCommands: 10,
  maxFeatures: 15,
  maxAPIs: 20,
});

